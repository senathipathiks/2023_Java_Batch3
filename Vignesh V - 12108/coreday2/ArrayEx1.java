package com.coreday2;

public class ArrayEx1 {
	public static void main(String[] args) {

		int arr[] = { 10, 20, 30, 40, 50 };
		String[] name = { "vicky", "sanjai", "surya", "vishnu" };
		for (String s : name) {

			System.out.println(s);
		}
		for (int i : arr) {
			System.out.println(i);
		}

	}

}
